function getDataList($data){


	try{
		$dbh = new Db();
		$query = "SELECT RevenueTypeId AS id, RevenueTypeName,Rate
		FROM t_revenue_type 
		ORDER BY `RevenueTypeName` ASC;";		
		
		$resultdata = $dbh->query($query);
		
		$returnData = [
			"success" => 1,
			"status" => 200,
			"message" => "",
			"datalist" => $resultdata
		];

	}catch(PDOException $e){
		$returnData = msg(0,500,$e->getMessage());
	}
	
	return $returnData;
}



function dataAddEdit($data) {

	if($_SERVER["REQUEST_METHOD"] != "POST"){
		return $returnData = msg(0,404,'Page Not Found!');
	}else{
		
		
		$lan = trim($data->lan); 
		$UserId = trim($data->UserId); 

		$RevenueTypeId = $data->rowData->id;
		$RevenueTypeName = $data->rowData->RevenueTypeName;
		$Rate = $data->rowData->Rate;

		try{

			$dbh = new Db();
			$aQuerys = array();

			if($RevenueTypeId == ""){
				$q = new insertq();
				$q->table = 't_revenue_type';
				$q->columns = ['RevenueTypeName','Rate'];
				$q->values = [$RevenueTypeName,$Rate];
				$q->pks = ['RevenueTypeId'];
				$q->bUseInsetId = false;
				$q->build_query();
				$aQuerys = array($q); 
			}else{
				$u = new updateq();
				$u->table = 't_revenue_type';
				$u->columns = ['RevenueTypeName','Rate'];
				$u->values = [$RevenueTypeName,$Rate];
				$u->pks = ['RevenueTypeId'];
				$u->pk_values = [$RevenueTypeId];
				$u->build_query();
				$aQuerys = array($u);
			}
			
			$res = exec_query($aQuerys, $UserId, $lan);  
			$success=($res['msgType']=='success')?1:0;
			$status=($res['msgType']=='success')?200:500;

			$returnData = [
			    "success" => $success ,
				"status" => $status,
				"UserId"=> $UserId,
				"message" => $res['msg']
			];

		}catch(PDOException $e){
			$returnData = msg(0,500,$e->getMessage());
		}
		
		return $returnData;
	}
}


